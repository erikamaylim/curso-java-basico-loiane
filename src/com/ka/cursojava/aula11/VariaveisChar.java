package com.ka.cursojava.aula11;

public class VariaveisChar {
    public static void main(String[] args) {

        char o1 = 'o';
        char i1 = 'i';
        char o = 111;
        char i = 105;
        char interrogacao = 0x003F;

        System.out.println(o1 + i1);
        System.out.println("" + o1 + i1);
        System.out.println("" + o + i + interrogacao);
    }
}
